function formaterTexte(texte) {
    // Divise le texte à chaque point
    let elements = texte.split('•');

    // Ajoute une nouvelle ligne avant chaque élément et conserve le point
    let resultat = elements.map(element => {
        return element.trim() !== '' ? '\n• ' + element.trim() : '';
    });

    // Retourne le texte formaté en tant que chaîne
    return resultat.join('');
}

